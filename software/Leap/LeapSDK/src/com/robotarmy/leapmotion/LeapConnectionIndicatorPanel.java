    // Called by Timer at about 30FPS
    @Override
    public void actionPerformed(ActionEvent ae) {
        if (controller.isConnected()) {
            if (!leapConnected) {
                leapConnected = true;
                statusPanel.setBackground(Color.GREEN);
            }
        } else {
            leapConnected = false;
            statusPanel.setBackground(Color.RED);
        }
        repaint();
    }

